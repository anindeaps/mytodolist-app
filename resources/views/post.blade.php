            <div class="row gx-4 gx-lg-5 justify-content-center">
                <div class="col-md-10 col-lg-8 col-xl-7">
                    <p>Dalam kehidupan sehari-hari, kita sering dihadapkan dengan berbagai tugas dan kewajiban yang
                        harus diselesaikan. Tugas-tugas tersebut bisa berupa pekerjaan, tugas sekolah, tugas rumah
                        tangga, atau sekadar hal-hal kecil yang perlu dilakukan.
                    </p>
                    <p>
                        Ketika kita memiliki banyak tugas yang harus diselesaikan, terkadang kita merasa kebingungan dan
                        tidak tahu harus memulai dari mana. Hal ini dapat menyebabkan kita merasa stres, tertekan, dan
                        bahkan gagal menyelesaikan tugas yang ada.
                    </p>
                    <p>
                        Salah satu cara untuk mengatasi masalah tersebut adalah dengan membuat to do list. To do list
                        adalah daftar tugas yang perlu diselesaikan dalam jangka waktu tertentu. Dengan membuat to do
                        list, kita dapat mengetahui apa saja yang harus dilakukan dan kapan harus diselesaikan.
                    </p>
                    <h2 class="section-heading">Manfaat Membuat To Do List</h2>
                    <p>Membuat to do list memiliki banyak manfaat, di antaranya:</p>

                    <h2 class="section-heading">1. Kamu tidak lupa dengan kegiatan kamu</h2>
                    <p>Kemampuan mengingat setiap orang berbeda-beda. Ada orang yang memiliki kemampuan mengingat yang
                        sangat baik, namun ada juga yang memiliki kemampuan mengingat yang kurang baik. To do list dapat
                        membantu orang-orang yang memiliki kemampuan mengingat yang kurang baik untuk tetap mengingat
                        semua tugas yang harus diselesaikan.</p>
                    <p>
                    <p>To do list juga dapat membantu kita untuk mengingat tugas-tugas yang tidak rutin kita lakukan.
                        Misalnya, jika kita memiliki tugas untuk membersihkan rumah setiap minggu, maka kita dapat
                        menuliskan tugas tersebut di to do list agar kita tidak lupa untuk melakukannya.</p>

                    <h2 class="section-heading">2. Semangat makin meningkat</h2>
                    <p>Membuat to do list dapat membantu kita untuk lebih bersemangat dalam menjalani hari-hari. Hal ini
                        dikarenakan kita mengetahui kegiatan apa saja yang akan kita lakukan selama hari itu, sehingga
                        kita dapat menyusun rangkaian aktivitas dengan baik dan menyesuaikannya agar satu aktivitas
                        dengan aktivitas yang lainnya tidak bertabrakan.</p>
                    <p>
                    <p>
                        Selain itu, membuat to do list juga dapat memberikan kita rasa percaya diri bahwa kita dapat
                        menyelesaikan semua tugas yang ada. Hal ini dapat meningkatkan semangat kita untuk menjalani
                        hari-hari.
                    </p>

                    <h2 class="section-heading">3. Kamu bisa lebih produktif</h2>
                    <p>
                        To do list dapat membantu kita untuk lebih produktif dalam menggunakan waktu. Dengan membuat to
                        do list, kita dapat memprioritaskan tugas-tugas yang harus diselesaikan terlebih dahulu,
                        sehingga kita tidak akan membuang waktu untuk mengerjakan tugas-tugas yang tidak penting.
                    </p>
                    <p>
                    <p>
                        Selain itu, to do list juga dapat membantu kita untuk lebih fokus pada tugas yang sedang
                        dikerjakan. Hal ini dapat meningkatkan produktivitas kita.
                    </p>


                    <h2 class="section-heading">4. Jadwal jadi lebih teratur</h2>
                    <p>
                        Selanjutnya adalah manfaat to do list adalah untuk membantu kita untuk mengatur jadwal dengan
                        lebih teratur. Dengan membuat to do list, kita dapat mengetahui apa saja yang harus dilakukan
                        dan kapan harus diselesaikan, sehingga kita dapat lebih mudah dalam menjalankan berbagai
                        aktivitas.
                    </p>
                    <p>
                    <p>
                        Selain itu, to do list juga dapat membantu kita untuk menghindari keterlambatan. Hal ini
                        dikarenakan kita dapat melihat jadwal yang harus kita ikuti dengan jelas.
                    </p>

                    <h2 class="section-heading">5. Belajar bagaimana manajemen waktu</h2>
                    <p>
                        Membuat to do list dapat membantu kita untuk belajar mengenai manajemen waktu. Dengan menuliskan
                        berbagai kegiatan yang akan kita lakukan, apalagi dilengkapi waktu yang rinci, kita dapat
                        belajar bagaimana mengatur waktu dengan lebih efektif dan efisien.
                    </p>
                    <p>
                    <p>
                        Selain itu, to do list juga dapat membantu kita untuk mengetahui berapa banyak waktu yang kita
                        miliki untuk setiap tugas. Hal ini dapat membantu kita untuk mengatur waktu dengan lebih baik.
                    </p>

                    <h2 class="section-heading">6. Membantu mengurangi stres</h2>
                    <p>
                        Dengan membuat to do list, kita dapat menyelesaikan tugas-tugas dengan lebih cepat dan tepat
                        waktu. Hal ini dapat membantu kita untuk mengurangi stres, karena kita tidak akan merasa
                        dikejar-kejar oleh deadline.
                    </p>
                    <p>
                    <p>
                        Selain itu, to do list juga dapat memberikan kita rasa puas dan bangga ketika kita berhasil
                        menyelesaikan semua tugas yang ada. Hal ini dapat mengurangi stres dan meningkatkan rasa bahagia
                        kita.
                    </p>

